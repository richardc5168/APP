function expandBank(bankPath) {
  if (!fs.existsSync(bankPath)) { console.log('SKIP', bankPath); return; }
  const src = fs.readFileSync(bankPath, 'utf8');
  const w = {};
  new Function('window', src)(w);
  const varMatch = src.match(/window\.(\w+)\s*=/);
  const varName = varMatch[1];
  const bank = w[varName];
  const existingIds = new Set(bank.map(q => q.id));
  let added = 0;
  for (const q of NEW_QUESTIONS) {
    if (!existingIds.has(q.id)) { bank.push(q); added++; }
  }
  const out = 'window.' + varName + ' = ' + JSON.stringify(bank, null, 2) + ';\n';
  fs.writeFileSync(bankPath, out, 'utf8');
  console.log('  ' + bankPath + ': ' + bank.length + ' total (+' + added + ')');
}

